
function Bold_Text(){
    var string_1 = "This string is Bold, and in red!"; 
    var result =  string_1.fontcolor("red"); 
    document.getElementById("Bold").innerHTML = result;
    
}
function Underlined_Text(){
    var string_2 = "This text is underlined, and in blue!" ;
    var result_1 = string_2.fontcolor("Blue");
    document.getElementById("Underline").innerHTML = result_1 ; 
}

